        [HttpPost]
        public async Task<ActionResult> ResetPassword(PasswordResetModel model)
        {
          bool value =  ModelState.ContainsKey("PhoneNumber");
            if (!ModelState.IsValid)
            {
                
                return View("Profile", new PasswordResetUserDetailsModel { passwordReset = model, UserDetails = new UserDetailsModel()});
            }
            string userId = User.Identity.GetUserId();
            if (string.IsNullOrEmpty(userId))
            {
                return RedirectToAction("Login", "Account");
            }

            ApplicationUser applicationUser = UserManager.FindById(User.Identity.GetUserId());
            
            bool isPasswordCorrect =  UserManager.CheckPassword(applicationUser, model.CurrentPassword);
            if(!isPasswordCorrect)
            {
              
                    SignInManager.AuthenticationManager.SignOut(DefaultAuthenticationTypes.ApplicationCookie);
                return RedirectToAction("Login", "Account");
            }
            var result = UserManager.ChangePassword(userId, model.CurrentPassword, model.NewPassword);

            if (result.Succeeded)
            {
                ViewBag.PasswordUpdateSuccess = CustomMessages.PasswordUpdateSuccess;
            }
            else
            {
                ModelState.AddModelError("", result.Errors.FirstOrDefault());
            }
            
            return View();
        }

